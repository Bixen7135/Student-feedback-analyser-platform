const missingTranslations = new Set<string>();

export function isLocale(value: string | null | undefined): value is Locale {
  return Boolean(value) && SUPPORTED_LOCALES.includes(value as Locale);
}

export function resolvePreferredLocale(preferred?: string | null): Locale {
  const candidates: string[] = [];

  if (preferred) {
    candidates.push(preferred);
  }

  if (typeof window !== "undefined" && Array.isArray(window.navigator.languages)) {
    candidates.push(...window.navigator.languages);
  }

  for (const candidate of candidates) {
    const parsedCandidates = candidate
      .split(",")
      .flatMap((entry) => entry.split(";"))
      .map((entry) => entry.trim().toLowerCase())
      .filter(Boolean);

    for (const normalized of parsedCandidates) {
      if (normalized.startsWith("en")) {
        return "en";
      }
      if (normalized.startsWith("kk")) {
        return "kk";
      }
      if (normalized.startsWith("ru")) {
        return "ru";
      }
    }
  }

  return DEFAULT_LOCALE;
}

export function normalizeTranslatableText(value: string): string {
  return value
    .replace(/\u00a0/g, " ")
    .replace(/\u2026/g, "...")
    .replace(/[\u2013\u2014]/g, "-")
    .replace(/\s+/g, " ")
    .trim();
}

function shouldTrackMissingTranslation(value: string): boolean {
  if (!/[A-Za-z]/.test(value)) {
    return false;
  }
  if (value.length < 3 || value.length > 160) {
    return false;
  }
  if (/^\/|^https?:\/\//i.test(value) || value.includes("/")) {
    return false;
  }
  if (!/[\s:,.!?-]/.test(value)) {
    return false;
  }
  if (/^[A-Z0-9_-]+$/.test(value)) {
    return false;
  }
  return true;
}

function recordMissingTranslation(value: string) {
  if (process.env.NODE_ENV === "production" || missingTranslations.has(value)) {
    return;
  }

  missingTranslations.add(value);

  if (typeof window !== "undefined") {
    const store = globalThis as typeof globalThis & {
      __SFAP_I18N_MISSING__?: string[];
    };
    const bucket = store.__SFAP_I18N_MISSING__ ?? [];
    if (!bucket.includes(value)) {
      bucket.push(value);
      store.__SFAP_I18N_MISSING__ = bucket;
    }
  }

  console.warn(`[i18n] Missing translation: ${value}`);
}

function translateCore(normalizedValue: string, locale: Locale): string | null {
  if (locale === "en") {
    return normalizedValue;
  }

  const exact =
    EXACT_TRANSLATIONS[normalizedValue] ??
    TARGET_PAGE_EXACT_TRANSLATIONS[normalizedValue];
  if (exact) {
    return exact[locale];
  }

  for (const entry of PATTERN_TRANSLATIONS) {
    const match = normalizedValue.match(entry.pattern);
    if (match) {
      return entry.format[locale](match);
    }
  }

  for (const entry of TARGET_PAGE_PATTERN_TRANSLATIONS) {
    const match = normalizedValue.match(entry.pattern);
    if (match) {
      return entry.format[locale](match);
    }
  }

  if (shouldTrackMissingTranslation(normalizedValue)) {
    recordMissingTranslation(normalizedValue);
  }

  return null;
}

export function translateText(value: string, locale: Locale): string {
  const boundaryMatch = value.match(/^(\s*)([\s\S]*?)(\s*)$/);
  if (!boundaryMatch) {
    return value;
  }

  const [, leading, body, trailing] = boundaryMatch;
  const normalized = normalizeTranslatableText(body);

  if (!normalized) {
    return value;
  }

  const translated = translateCore(normalized, locale);
  if (!translated) {
    return value;
  }

  return `${leading}${translated}${trailing}`;
}

